bool CheMPS2::Wigner::triangle_fails( const int two_ja, const int two_jb, const int two_jc ){

   return (( ( two_ja + two_jb + two_jc ) % 2 != 0 ) || ( two_ja + two_jb < two_jc ) || ( abs( two_ja - two_jb ) > two_jc ));

}

void CheMPS2::Wigner::delta( const int two_ja, const int two_jb, const int two_jc, short * pow ){

   const int val1 = (  two_ja + two_jb - two_jc ) / 2;
   const int val2 = (  two_ja - two_jb + two_jc ) / 2;
   const int val3 = ( -two_ja + two_jb + two_jc ) / 2;
   const int val4 = (  two_ja + two_jb + two_jc ) / 2 + 1;

   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val1 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val2 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val3 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val4 ].pow[ cnt ]; }

}

void CheMPS2::Wigner::delta3j( const int two_ja, const int two_jb, const int two_jc, const int two_ma, const int two_mb, const int two_mc, short * pow ){

   const int val1 = ( two_ja + two_ma ) / 2;
   const int val2 = ( two_ja - two_ma ) / 2;
   const int val3 = ( two_jb + two_mb ) / 2;
   const int val4 = ( two_jb - two_mb ) / 2;
   const int val5 = ( two_jc + two_mc ) / 2;
   const int val6 = ( two_jc - two_mc ) / 2;

   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val1 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val2 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val3 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val4 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val5 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] += factorials[ val6 ].pow[ cnt ]; }

}

void CheMPS2::Wigner::term3j( const int alpha1, const int alpha2, const int beta1, const int beta2, const int beta3, const int k, short * pow ){

   const int val1 = k;
   const int val2 = k - alpha1;
   const int val3 = k - alpha2;
   const int val4 = beta1 - k;
   const int val5 = beta2 - k;
   const int val6 = beta3 - k;

   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ]  = -factorials[ val1 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -=  factorials[ val2 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -=  factorials[ val3 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -=  factorials[ val4 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -=  factorials[ val5 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -=  factorials[ val6 ].pow[ cnt ]; }

}

void CheMPS2::Wigner::term6j( const int alpha1, const int alpha2, const int alpha3, const int alpha4, const int beta1, const int beta2, const int beta3, const int k, short * pow ){

   const int val1 = k + 1;
   const int val2 = k - alpha1;
   const int val3 = k - alpha2;
   const int val4 = k - alpha3;
   const int val5 = k - alpha4;
   const int val6 = beta1 - k;
   const int val7 = beta2 - k;
   const int val8 = beta3 - k;

   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ]  = factorials[ val1 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val2 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val3 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val4 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val5 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val6 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val7 ].pow[ cnt ]; }
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ pow[ cnt ] -= factorials[ val8 ].pow[ cnt ]; }

}

void CheMPS2::Wigner::shift_sqrt( short * target, prime_powers * terms, const int num_terms ){

   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){

      short fraction  = target[ cnt ] / 2;
      short remainder = target[ cnt ] - 2 * fraction;
      if ( remainder == -1 ){
         fraction -= 1;
         remainder = 1;
      }
      target[ cnt ] = remainder;
      for ( int term = 0; term < num_terms; term++ ){
         terms[ term ].pow[ cnt ] += fraction;
      }

   }

}

void CheMPS2::Wigner::extract_denominator( short * denom, prime_powers * terms, const int num_terms ){

   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){

      short minimal_value = 0;
      for ( int term = 0; term < num_terms; term++ ){
         minimal_value = min( minimal_value, terms[ term ].pow[ cnt ] );
      }
      denom[ cnt ] = -minimal_value;
      for ( int term = 0; term < num_terms; term++ ){
         terms[ term ].pow[ cnt ] -= minimal_value;
      }

   }

}

long double CheMPS2::Wigner::form_number_simple( short * pow ){

   long double value = 1.0;
   for ( short cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){
      for ( short alpha = 0; alpha < pow[ cnt ]; alpha++ ){
         value *= prime_numbers[ cnt ];
      }
   }
   return value;

}

double CheMPS2::Wigner::wigner3j( const int two_ja, const int two_jb, const int two_jc, const int two_ma, const int two_mb, const int two_mc ){

   assert( ( two_ja >= 0        ) && ( two_jb >= 0        ) && ( two_jc >= 0 )        );
   assert( ( two_ja <= max_2j() ) && ( two_jb <= max_2j() ) && ( two_jc <= max_2j() ) );

   if (( triangle_fails( two_ja, two_jb, two_jc ) ) ||
       ( ( two_ja + two_ma ) % 2 != 0 ) ||
       ( ( two_jb + two_mb ) % 2 != 0 ) ||
       ( ( two_jc + two_mc ) % 2 != 0 ) ||
       ( two_ma > two_ja ) ||
       ( two_mb > two_jb ) ||
       ( two_mc > two_jc ) ||
       ( two_ma < -two_ja ) ||
       ( two_mb < -two_jb ) ||
       ( two_mc < -two_jc ) ||
       ( two_ma + two_mb + two_mc != 0 )){ return 0.0; }

   const int alpha1 = ( two_jb - two_jc - two_ma ) / 2;
   const int alpha2 = ( two_ja - two_jc + two_mb ) / 2;
   const int beta1  = ( two_ja + two_jb - two_jc ) / 2;
   const int beta2  = ( two_ja - two_ma ) / 2;
   const int beta3  = ( two_jb + two_mb ) / 2;

   const int max_alpha = max( 0, max( alpha1, alpha2 ) );
   const int min_beta  = min( beta1, min( beta2, beta3 ) );
   if ( min_beta < max_alpha ){ return 0.0; }
   const int num_terms = min_beta - max_alpha + 1;

   prime_powers deltas;
   prime_powers terms[ num_terms ];
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ deltas.pow[ cnt ] = 0; }
   delta( two_ja, two_jb, two_jc, deltas.pow );
   delta3j( two_ja, two_jb, two_jc, two_ma, two_mb, two_mc, deltas.pow );

   for ( int term = 0; term < num_terms; term++ ){
      term3j( alpha1, alpha2, beta1, beta2, beta3, term + max_alpha, terms[ term ].pow );
   }

   shift_sqrt( deltas.pow, terms, num_terms );
   const double wigner_3j_factor1 = sqrt( form_number_simple( deltas.pow ) ); // sqrt( s )

   extract_denominator( deltas.pow, terms, num_terms );
   const double wigner_3j_factor2 = form_number_simple( deltas.pow ); // q

   const int global_phase = ((((( two_ja - two_jb - two_mc ) / 2 ) % 2 ) != 0 ) ? -1 : 1 );
   long double wigner_3j_factor3 = 0.0; // n
   for ( int term = 0; term < num_terms; term++ ){
      wigner_3j_factor3 += (( ( max_alpha + term ) % 2 == 0 ) ? 1 : -1 ) * global_phase * form_number_simple( terms[ term ].pow );
   }

   return ( double )( wigner_3j_factor3 * wigner_3j_factor1 / wigner_3j_factor2 );

}

double CheMPS2::Wigner::wigner6j( const int two_ja, const int two_jb, const int two_jc, const int two_jd, const int two_je, const int two_jf ){

   assert( ( two_ja >= 0        ) && ( two_jb >= 0        ) && ( two_jc >= 0        ) && ( two_jd >= 0        ) && ( two_je >= 0        ) && ( two_jf >= 0        ) );
   assert( ( two_ja <= max_2j() ) && ( two_jb <= max_2j() ) && ( two_jc <= max_2j() ) && ( two_jd <= max_2j() ) && ( two_je <= max_2j() ) && ( two_jf <= max_2j() ) );

   if (( triangle_fails( two_ja, two_jb, two_jc ) ) ||
       ( triangle_fails( two_jd, two_je, two_jc ) ) ||
       ( triangle_fails( two_ja, two_je, two_jf ) ) ||
       ( triangle_fails( two_jd, two_jb, two_jf ) )){ return 0.0; }

   const int alpha1 = ( two_ja + two_jb + two_jc ) / 2;
   const int alpha2 = ( two_jd + two_je + two_jc ) / 2;
   const int alpha3 = ( two_ja + two_je + two_jf ) / 2;
   const int alpha4 = ( two_jd + two_jb + two_jf ) / 2;
   const int beta1  = ( two_ja + two_jb + two_jd + two_je ) / 2;
   const int beta2  = ( two_ja + two_jc + two_jd + two_jf ) / 2;
   const int beta3  = ( two_jb + two_jc + two_je + two_jf ) / 2;

   const int max_alpha = max( alpha1, max( alpha2, max( alpha3, alpha4 ) ) );
   const int min_beta  = min( beta1, min( beta2, beta3 ) );
   if ( min_beta < max_alpha ){ return 0.0; }
   const int num_terms = min_beta - max_alpha + 1;

   prime_powers deltas;
   prime_powers terms[ num_terms ];
   for ( int cnt = 0; cnt < CHEMPS2_WIGNER_NUM_PRIMENUMS; cnt++ ){ deltas.pow[ cnt ] = 0; }
   delta( two_ja, two_jb, two_jc, deltas.pow );
   delta( two_jd, two_je, two_jc, deltas.pow );
   delta( two_ja, two_je, two_jf, deltas.pow );
   delta( two_jd, two_jb, two_jf, deltas.pow );

   for ( int term = 0; term < num_terms; term++ ){
      term6j( alpha1, alpha2, alpha3, alpha4, beta1, beta2, beta3, term + max_alpha, terms[ term ].pow );
   }

   shift_sqrt( deltas.pow, terms, num_terms );
   const double wigner_6j_factor1 = sqrt( form_number_simple( deltas.pow ) ); // sqrt( s )

   extract_denominator( deltas.pow, terms, num_terms );
   const double wigner_6j_factor2 = form_number_simple( deltas.pow ); // q

   long double wigner_6j_factor3 = 0.0; // n
   for ( int term = 0; term < num_terms; term++ ){
      wigner_6j_factor3 += (( ( max_alpha + term ) % 2 == 0 ) ? 1 : -1 ) * form_number_simple( terms[ term ].pow );
   }

   return ( double )( wigner_6j_factor3 * wigner_6j_factor1 / wigner_6j_factor2 );

}

double CheMPS2::Wigner::wigner9j( const int two_ja, const int two_jb, const int two_jc, const int two_jd, const int two_je, const int two_jf, const int two_jg, const int two_jh, const int two_ji ){

   assert( ( two_ja >= 0        ) && ( two_jb >= 0        ) && ( two_jc >= 0        ) &&
           ( two_jd >= 0        ) && ( two_je >= 0        ) && ( two_jf >= 0        ) &&
           ( two_jg >= 0        ) && ( two_jh >= 0        ) && ( two_ji >= 0        ) );
   assert( ( two_ja <= max_2j() ) && ( two_jb <= max_2j() ) && ( two_jc <= max_2j() ) &&
           ( two_jd <= max_2j() ) && ( two_je <= max_2j() ) && ( two_jf <= max_2j() ) &&
           ( two_jg <= max_2j() ) && ( two_jh <= max_2j() ) && ( two_ji <= max_2j() ) );

   if (( triangle_fails( two_ja, two_jb, two_jc ) ) ||
       ( triangle_fails( two_jd, two_je, two_jf ) ) ||
       ( triangle_fails( two_jg, two_jh, two_ji ) ) ||
       ( triangle_fails( two_ja, two_jd, two_jg ) ) ||
       ( triangle_fails( two_jb, two_je, two_jh ) ) ||
       ( triangle_fails( two_jc, two_jf, two_ji ) )){ return 0.0; }

   const int lower_2j = max( abs( two_ja - two_ji ), max( abs( two_jh - two_jd ), abs( two_jb - two_jf ) ) );
   const int upper_2j = min(      two_ja + two_ji,   min(      two_jh + two_jd,        two_jb + two_jf ) );

   double value = 0.0;
   for ( int sum_2j = lower_2j; sum_2j <= upper_2j; sum_2j += 2 ){
      const double val1 = wigner6j( two_ja, two_jb, two_jc, two_jf, two_ji, sum_2j );
      const double val2 = wigner6j( two_jd, two_je, two_jf, two_jb, sum_2j, two_jh );
      const double val3 = wigner6j( two_jg, two_jh, two_ji, sum_2j, two_ja, two_jd );
      value += ( sum_2j + 1 ) * val1 * val2 * val3;
   }
   const int phase = (( lower_2j % 2 == 0 ) ? 1 : -1 );

   return ( value * phase );

}


